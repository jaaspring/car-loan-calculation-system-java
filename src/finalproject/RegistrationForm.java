    private void signUpButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpButtonActionPerformed
        String username = usernameTextField.getText().trim();
        String name = nameTextField.getText().trim();
        String email = emailTextField.getText().trim();
        String phone = phoneTextField.getText().trim();
        String password = new String(passwordField.getPassword());

        try {
            // Validation
            if (username.isEmpty()) {
                throw new RegistrationException("Username is required.");
            }
            if (name.isEmpty()) {
                throw new RegistrationException("Name is required.");
            }
            if (email.isEmpty() || !email.matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$")) {
                throw new RegistrationException("Enter a valid email address.");
            }
            if (phone.isEmpty() || !phone.matches("\\d{10,12}")) {
                throw new RegistrationException("Enter a valid phone number (10-12 digits).");
            }
            if (password.isEmpty() || password.length() < 6) {
                throw new RegistrationException("Password must be at least 6 characters long.");
            }

            // Hash the password using bcrypt
            String hashedPassword = BCrypt.hashpw(password, BCrypt.gensalt());

            // Database Connection
            try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/loan_calculator", "root", "");
                 PreparedStatement pst = con.prepareStatement("INSERT INTO users (username, name, email, phone, password) VALUES (?, ?, ?, ?, ?)")) {

                pst.setString(1, username);
                pst.setString(2, name);
                pst.setString(3, email);
                pst.setString(4, phone);
                pst.setString(5, hashedPassword);  // Save the hashed password

                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "User registered successfully!");
                this.dispose();
                new LoginForm().setVisible(true);

            } catch (SQLIntegrityConstraintViolationException e) {
                throw new RegistrationException("Username already exists!");
            } catch (SQLException e) {
                throw new RegistrationException("Database connection error!");
            }

        } catch (RegistrationException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Registration Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_signUpButtonActionPerformed

