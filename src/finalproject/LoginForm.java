    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String username = usernameTextField.getText().trim();
        String password = new String(passwordField.getPassword());

        try {
            if (username.isEmpty()) {
                throw new LoginException("Username is required.");
            }
            if (password.isEmpty()) {
                throw new LoginException("Password is required.");
            }

            // Database Connection
            try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/loan_calculator", "root", "");
                 PreparedStatement pst = con.prepareStatement("SELECT * FROM users WHERE username = ?")) {

                pst.setString(1, username);
                ResultSet rs = pst.executeQuery();

                if (rs.next()) { // User exists
                    String storedHashedPassword = rs.getString("password");
                    String role = rs.getString("role");

                    if (BCrypt.checkpw(password, storedHashedPassword)) { 
                        String userId = rs.getString("id");

                        if (role.equals("admin")) {
                            JOptionPane.showMessageDialog(this, "Admin login successful!");
                            this.dispose();
                            new AdminDashboardFrame(userId, username).setVisible(true);
                        } else {
                            JOptionPane.showMessageDialog(this, "Login successful!");
                            this.dispose();
                            new HomeFrame(userId, username).setVisible(true);
                        }
                    } else {
                        throw new LoginException("Invalid username or password.");
                    }
                } else {
                    throw new LoginException("Invalid username or password.");
                }

            } catch (SQLException e) {
                throw new LoginException("Database connection error!");
            }

        } catch (LoginException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Login Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_loginButtonActionPerformed

