  private void deleteHistory(int recordId) {
    try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/loan_calculator", "root", "");
         PreparedStatement pst = con.prepareStatement("DELETE FROM loan_history WHERE id = ?")) {

        pst.setInt(1, recordId); // Use the unique record ID

        int rowsAffected = pst.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Record deleted successfully!");
            loadHistoryData(); // Refresh the table
        } else {
            JOptionPane.showMessageDialog(this, "Record not found in the database.");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error deleting record: " + e.getMessage());
    }
}

 private void loadHistoryData() {
    DefaultTableModel model = (DefaultTableModel) historyTable.getModel();
    model.setRowCount(0); // Clear existing rows

    try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/loan_calculator", "root", "");
         PreparedStatement pst = con.prepareStatement("SELECT * FROM loan_history WHERE user_id = ?")) {
        pst.setString(1, userId);

        ResultSet rs = pst.executeQuery();
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),  // Add the id column here
                rs.getString("vehicle"),
                rs.getString("variant"),
                rs.getString("paint_type"),
                rs.getInt("terms"),
                rs.getDouble("down_payment"),
                rs.getDouble("interest_rate"),
                rs.getDouble("monthly_installment"),
                "Delete"
            });
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error loading history: " + e.getMessage());
    }
}

