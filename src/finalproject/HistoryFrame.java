    private void historyTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_historyTableMouseClicked
   
    int row = historyTable.getSelectedRow(); // Get the clicked row index
    int column = historyTable.getSelectedColumn(); // Get the clicked column index

    // Ensure the correct column is selected for deletion
    if (column == 8) { // Assuming "Delete" is in the 7th column (index 7)
        // Show confirmation dialog
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this record?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            // Get the ID from the first column (which should be the unique ID for each record)
            String idStr = historyTable.getValueAt(row, 0).toString(); // Get the ID from the first column
            try {
                int recordId = Integer.parseInt(idStr); // Convert it to an integer
                // Call delete method
                deleteHistory(recordId);
            } catch (NumberFormatException e) {
                // Handle invalid ID format
                JOptionPane.showMessageDialog(this, "Invalid record ID. Please try again.");
            }
        }
    
    }
    
    
    }//GEN-LAST:event_historyTableMouseClicked

