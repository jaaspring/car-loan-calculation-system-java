    // Method to load image into JLabel from database path
    private void setImageToLabel(JLabel label, String imagePath) {
        if (imagePath == null || imagePath.isEmpty()) {
            System.out.println("No image path found!");
            label.setText("No Image");
            return;
        }

        try {
            // Load image from resources
            java.net.URL imgURL = getClass().getClassLoader().getResource(imagePath);

            if (imgURL != null) {
                ImageIcon icon = new ImageIcon(imgURL);
                Image scaledImage = icon.getImage().getScaledInstance(label.getWidth(), label.getHeight(), Image.SCALE_SMOOTH);
                label.setIcon(new ImageIcon(scaledImage));
                System.out.println("Image loaded: " + imagePath);
            } else {
                System.out.println("ERROR: Image NOT FOUND -> " + imagePath);
                label.setText("Image Not Found");
            }
        } catch (Exception e) {
            e.printStackTrace();
            label.setText("Error Loading Image");
        }
    }
    
    // Fetch image path from the database based on selected car model
    private String getCarImagePath(String model) {
        String imagePath = "";
        String query = "SELECT image_path FROM car_details WHERE model = ? LIMIT 1";

        try (Connection conn = DBConnection.getConnection();
             PreparedStatement stmt = conn.prepareStatement(query)) {

            stmt.setString(1, model);
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                imagePath = rs.getString("image_path");
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        }

        return imagePath;
    }

